#include <iostream>
using namespace std;

int main() {
	int y = 8, x = 6;
	x += 5;
	cout <<"The current value of x: "<< x << "\n";
	y -= 2;
	cout <<"The current value of y: " << y << "\n";
	x *= 2;
	cout <<"The current value of x: " << x << "\n";
	y /= 2;
	cout <<"The current value of y: " << y << "\n";
	return 0;
}